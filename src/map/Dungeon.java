package map;

import java.util.List;

/**
 *
 * @author Kurumin
 */
public class Dungeon {
    
    private String id;
    private List<Level> levels;

    public Dungeon(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public List<Level> getLevels() {
        return levels;
    }

    public void setLevels(List<Level> levels) {
        this.levels = levels;
    }
}
